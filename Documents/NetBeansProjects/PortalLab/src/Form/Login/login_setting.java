    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String pilih = JOptionPane.showInputDialog("Tambah akun \n1.STAFF \n2.STRUKTUR ASLAB.");
        if (pilih.equals("1")) {
            String kode = JOptionPane.showInputDialog("Masukan Kode Admin untuk melanjutkan.");
            if (kode.equals("addstaff")) {
                tambah_staff stf = new tambah_staff();
                stf.setVisible(true);
                setVisible(false);    
            }else{;
                JOptionPane.showMessageDialog(null, "KODE YANG ANDA MASUKAN SALAH.\nSEGERA HUBUNGI ADMIN UNTUK MEMASUKKAN KODE!", "ERROR", JOptionPane.WARNING_MESSAGE);
            }   
        }
        else if (pilih.equals("2")) {
            String kode = JOptionPane.showInputDialog("Masukan Kode Admin untuk melanjutkan.");
            if (kode.equals("addaslab")) {
                tambah_struk str = new tambah_struk();
                str.setVisible(true);
                setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "KODE YANG ANDA MASUKAN SALAH.\nSEGERA HUBUNGI ADMIN UNTUK MEMASUKKAN KODE!", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
            
        }
        
        else{
            JOptionPane.showMessageDialog(null, "TIDAK ADA PILIHAN, MOHON SEK KEMBALI!", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

